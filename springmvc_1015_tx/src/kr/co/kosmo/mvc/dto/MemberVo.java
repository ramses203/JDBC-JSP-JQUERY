package kr.co.kosmo.mvc.dto;

public class MemberVo {
	private int num;
	private String id;
	private String pwd;
	private String name;
	private int age;
	private int mdate;
	private int tel;
	
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getMdate() {
		return mdate;
	}
	public void setMdate(int mdate) {
		this.mdate = mdate;
	}
	public int getTel() {
		return tel;
	}
	public void setTel(int tel) {
		this.tel = tel;
	}
	
	
	
	
	
	
}
