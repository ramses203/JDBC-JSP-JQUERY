package lol.mvc.dto;

public class ChampVO {

	private int cno;
	private String cname;
	private String cimg;
	private int catk;
	private int cdef;
	private int chp;
	private String line;

	public int getCno() {
		return cno;
	}

	public void setCno(int cno) {
		this.cno = cno;
	}

	public String getCname() {
		return cname;
	}

	public void setCname(String cname) {
		this.cname = cname;
	}

	public String getCimg() {
		return cimg;
	}

	public void setCimg(String cimg) {
		this.cimg = cimg;
	}

	public int getCatk() {
		return catk;
	}

	public void setCatk(int catk) {
		this.catk = catk;
	}

	public int getCdef() {
		return cdef;
	}

	public void setCdef(int cdef) {
		this.cdef = cdef;
	}

	public int getChp() {
		return chp;
	}

	public void setChp(int chp) {
		this.chp = chp;
	}

	public String getLine() {
		return line;
	}

	public void setLine(String line) {
		this.line = line;
	}

}
