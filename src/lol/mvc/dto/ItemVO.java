package lol.mvc.dto;

public class ItemVO {

	private int ino;
	private String iname;
	private String iimg;
	private int iatk;
	private int idef;
	private int ihp;

	public int getIno() {
		return ino;
	}

	public void setIno(int ino) {
		this.ino = ino;
	}

	public String getIname() {
		return iname;
	}

	public void setIname(String iname) {
		this.iname = iname;
	}

	public String getIimg() {
		return iimg;
	}

	public void setIimg(String iimg) {
		this.iimg = iimg;
	}

	public int getIatk() {
		return iatk;
	}

	public void setIatk(int iatk) {
		this.iatk = iatk;
	}

	public int getIdef() {
		return idef;
	}

	public void setIdef(int idef) {
		this.idef = idef;
	}

	public int getIhp() {
		return ihp;
	}

	public void setIhp(int ihp) {
		this.ihp = ihp;
	}

}
